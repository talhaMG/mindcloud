        <h1>What are the different types of income?</h1><br>

<label>Employment</label>
<p>You receive income (usually in the form of paychecks or W-2s) from an organization that you work for, but don't own.</p>

<label>Independent Contractor</label>
<p>You have temporary employment agreements and a US federal tax return Form 1099 is issued to you. Income is usually reported on Schedule C of US federal tax returns.</p>

<label>Military Pay</label>
<p>You receive a paycheck for currently serving in the military. Military pension/retirement income should be added as pension income, not as military pay.</p>

<label>Rental</label>
<p>You own a property and receive rental income from this property. Income is reported on Schedule E of US federal tax returns.</p>

<label>Social Security</label>
<p>You receive benefits from the US Social Security Administration.</p>

<label>Pension/retirement</label>
<p>You receive income from a defined pension, retirement, IRA, or 401k program.</p>

<label>Self-employment</label>
<p>You own a business or receive income from a business that you own 25% of or more. Income is usually reported on Schedule C and/or Schedule E of US federal tax returns.</p>

<label>Farm Income</label>
<p>You own and/or rent real estate upon which you conduct farming activity. You may also own farm land that you rent to others. This income is usually reported on Schedule F or Form 4835 of the U.S. federal tax returns. It may also be reported through Schedule E of the U.S. federal tax returns. You may also reported it on a Form 1120 if the farm is structured as a C Corporation. If you have Farm Income enter this income under Business/Self Employment.</p>

<label>Other</label>
<p>You receive income from any other source not mentioned above (such as disability, alimony, child support, trust, dividends, or interest).</p>

<p>You aren’t required to include alimony, child support, or separate maintenance income unless you want it considered as a basis for repaying a loan.</p>

